    public void render(ShapeRenderer shapeRenderer) {
        shapeRenderer.setColor(0, 0, 1, 1); // Blue color for walls
        for (int i = 0; i < maze.length; i++) {
            for (int j = 0; j < maze[i].length; j++) {
                if (maze[i][j] == 1) {
                    shapeRenderer.rect(j * tileSize, i * tileSize, tileSize, tileSize);
                }
            }
        }
    }

    // Check if a position is a wall
    public boolean isWall(float x, float y) {
        int row = (int) (y / tileSize);
        int col = (int) (x / tileSize);
        if (row >= 0 && row < maze.length && col >= 0 && col < maze[0].length) {
            return maze[row][col] == 1;
        }
        return true; // Treat out-of-bounds as walls
    }

    // Check collision with Pac-Man's bounds
    public boolean collidesWithWall(float x, float y, float radius) {
        // Check multiple points around Pac-Man's circle
        float[] pointsX = {x - radius, x + radius, x, x};
        float[] pointsY = {y, y, y - radius, y + radius};
        for (int i = 0; i < pointsX.length; i++) {
            if (isWall(pointsX[i], pointsY[i])) {
                return true;
            }
        }
        return false;
    }

    // Get the maze layout
    public int[][] getMazeLayout() {
        return maze;
    }

    // Get the tile size
    public int getTileSize() {
        return tileSize;
    }
